public class Variables {
	public static void main (String args []){
		//usando la preincrementacion ++a
		int variable1 = 5;
		System.out.println ("Valor inicial de la variable " + variable1);
		System.out.println ("valor de la variable incrementada " + ++variable1);
		//Usando la posincrementacion a++
		int variable2 = 5;
		System.out.println ("Valor inicial de la variable " + variable2);
		System.out.println ("valor de la variable incrementada " + variable2++);
		System.out.println ("El valor de la variable incrementada es: " + variable2);
		}
	}
